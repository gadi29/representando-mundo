public class Teacher {
  private String name;
  private String cpf;
  private int age;
  private String gender;

  String getName() {
    return this.name;
  }

  void setName(String name) {
    this.name = name;
  }

  String getCPF() {
    return this.cpf;
  }

  void setCPF(String cpf) {
    this.cpf = cpf;
  }

  int getAge() {
    return this.age;
  }

  void setAge(int age) {
    this.age = age;
  }

  String getGender() {
    return this.gender;
  }

  void setGender(String gender) {
    this.gender = gender;
  }
}
